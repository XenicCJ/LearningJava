//主类的声明，public权限，class类型以及类名
public class HelloWorld {
    //主方法，公开的、静态的、无返回值的、含有字符串数组参数的
    public static void main(String[] args){
        //最主要的向控制台输出的方法
        System.out.println("Hello World!");
        System.out.println("Hello World!!");
        System.out.println("Hello World!!!");
        System.out.println("Hello World!!!!");
        System.out.println("Hello World!!!!!");
    }
}
